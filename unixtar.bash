#-------------------------------------------------------------------------------
# copy config file and setup script
#-------------------------------------------------------------------------------
mkdir .vim/
cp vimrc setup.bash .vim/
#-------------------------------------------------------------------------------
# copy all bundles to .vim directory
#-------------------------------------------------------------------------------
find bundle/ -maxdepth 1 -print | sed -e '/bundle\/$/d' | xargs -I {} sh -c "cp -Rf ./{}/* .vim/"
#-------------------------------------------------------------------------------
# compress everything
#-------------------------------------------------------------------------------
tar -cvf vim.tar .vim/
#-------------------------------------------------------------------------------
# 
#-------------------------------------------------------------------------------
rm -rvf .vim/
